#include<stdio.h>
int main()
{
// i++ == It is an increment operator !! where i++ means i=i+1

    int i=5;
    printf("Your value of i++ is %d\n", i++);
    printf("Your value of i is %d ", i);
    // i++ = First print then increment 
    // ++i = First increment then print

    return 0;

// i-- is an decrement operator !! where i-- means i=i-1
// Follow same code as increment operator ..

// Compound operator ...
// i+=10 means i=i+10
// i+=5 means i=i+5 
}